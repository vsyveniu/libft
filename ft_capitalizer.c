#include "libft.h"

char	*ft_capitalizer(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((i == 0) && (str[i] >= 'a' && str[i] <= 'z'))
		{
			str[i] = str[i] - 32;
			i++;
		}
		if ((str[i] >= 'a' && str[i] <= 'z') && (str[i - 1] == ' '
			|| str[i - 1] == '\t' || str[i - 1] == '\n'))
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
